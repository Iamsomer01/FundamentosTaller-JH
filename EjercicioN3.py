""" Convertir los grados centígrados ingresados 
por un usuario a grados Fahrenheit y mostrar 
el resultado en pantalla."""

#programa que convierta los grados centígrados a grados Fahrenheit

print("programa que convierta los grados centígrados a grados Fahrenheit")
Centigrados = float(input("Ingrese los grados cenígrados que desea convertir: "))
FahFahrenheit = (Centigrados*9/5)+32
print ("La conversion de ", Centigrados,"grados centigrados grados Fahrenheit es: ",FahFahrenheit)




