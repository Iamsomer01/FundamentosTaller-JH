"""Dado de los valores ingresados 
por el usuario (base, altura) calcular y mostrar 
en pantalla el área de un triángulo."""

#Programa que calcula el area de un triangulo

print ("Programa que calcula el area de un triangulo")

print ("Cual es la base del triangulo: ")
base=int(input())
print ("Cual es la altura del triangulo: ")
altura=int(input())
area=  (base) * (altura) / 2
print ("el resultado es: ", area)


